    private void fnt_guardar_pedido() {
        fnt_consultar(txt_id1.getText(), "4");
        if (activo == 0) {
            JOptionPane.showMessageDialog(null, "Error, el cliente se encuentra inactivo");
        }
        if (activo == 1) {
            List<String> errores = new ArrayList<>();
            if (fnt_validaridentidicacion(txt_id.getText()) == false) {
                errores.add("Identificacion");
            }
            if (fnt_validardetalle(txt_detalleOrden.getText()) == false) {
                errores.add("Detalle");
            }
            if (fnt_validar_totalPagar(txt_totalPagar.getText()) == false) {
                errores.add("Total a Pagar");
            }

            if (errores.isEmpty() == false) {
                String mensajeError = "Se encontraron errores:\n" + String.join("\n", errores);
                JOptionPane.showMessageDialog(null, mensajeError);
                return;
            }
            objValidar2.validarDatosEntrada(txt_id1.getText(), txt_detalleOrden.getText(), txt_totalPagar.getText());

            clientes.add(new cls_clientes(txt_id.getText(), txt_nombre.getText(), txt_contacto.getText(),
                    txt_correo.getText()));

            JOptionPane.showMessageDialog(null, objValidar2.getMsn());
        }

    }

