    private void fnt_consultar(String id, String tipo) {
        sw = false;
        posicion = 0;
        for (int i = 0; i < clientes.size(); i++) {
            if (id.equals(clientes.get(i).getIdStr())) {
                sw = true;
                posicion = i;
                break;
            }

        }
        if (sw == false) {
            if (!tipo.equals("4")) {
                JOptionPane.showMessageDialog(null, "No se encontraron datos");
            }

            if (tipo.equals("3")) {
                txt_nombre1.setText("");
            }

        } else {
            if (tipo.equals("1")) {
                txt_contacto.setText(clientes.get(posicion).getContactoStr());
                txt_correo.setText(clientes.get(posicion).getCorreoStr());
                txt_nombre.setText(clientes.get(posicion).getNombreStr());
            }
            if (tipo.equals("2")) {
                clientes.get(posicion).setEstado(1);
                JOptionPane.showMessageDialog(null, "Estado actualizado");
            }
            if (tipo.equals("3")) {
                txt_id1.setEnabled(false);
                btn_guardarPedido.setEnabled(true);
                txt_nombre1.setText(clientes.get(posicion).getNombreStr());
            }
            if (tipo.equals("4")) {
                activo = clientes.get(posicion).getEstado();

            }
        }

    }

